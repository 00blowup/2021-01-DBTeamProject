-- 데이터 베이스 생성
CREATE DATABASE DB2021Team10;
-- 데이터 베이스 지정
USE DB2021Team10;

-- 테이블 생성 

-- 감독 테이블
CREATE TABLE DB2021_감독(
    이름 varchar(40) not null,
    생년월일 date,
    데뷔작 varchar(40),
    데뷔_연도 int,
    작품_개수 int,
    대표작 varchar(40),
    primary key(이름, 생년월일, 데뷔작)
);

-- 영화 테이블
CREATE TABLE DB2021_영화(
	제목 varchar(40) not null,
	감독 varchar(40) not null,
	장르 varchar(40),
	줄거리 varchar(5000),
    대표_배우 varchar(40),
	개봉_날짜 DATE,
	국가 varchar(40),
	러닝_타임 int,
	현재_상영 BOOLEAN,
	primary key (제목, 감독),
    foreign key (감독) references DB2021_감독(이름) 
    on delete cascade 
    on update cascade
);
CREATE INDEX idx_제목 ON DB2021_영화(제목);
CREATE INDEX idx_감독 ON DB2021_영화(감독);

-- 유통 테이블
CREATE TABLE DB2021_유통(
	제목 varchar(40) not null,
	감독 varchar(40) not null,
	DVD_제공 BOOLEAN,
	배급사 varchar(40),
	primary key (제목, 감독),
    foreign key (제목, 감독) references DB2021_영화(제목, 감독)
    on delete cascade 
    on update cascade
);

-- 스트리밍 테이블
CREATE TABLE DB2021_스트리밍 (
    제목 varchar(40) not null,
    감독 varchar(40) not null,
    서비스 varchar(10) not null,
    primary key (제목, 감독, 서비스),
    foreign key (제목, 감독) references DB2021_유통(제목, 감독) 
    on delete cascade 
    on update cascade,
    check(서비스 in ('넷플릭스', '왓챠', '웨이브', '티빙'))
);

-- 수익 테이블
CREATE TABLE DB2021_수익(
	제목 varchar(40) not null,
	감독 varchar(40) not null,
	매출 bigint,
	관객_수 bigint,
	손익분기점_도달 BOOLEAN,
	primary key (제목, 감독),
    foreign key (제목, 감독) references DB2021_영화(제목, 감독) 
    on delete cascade 
    on update cascade
);

-- 평점 테이블
CREATE TABLE DB2021_평점(
    제목 varchar(40),
    감독 varchar(40),
    네이버 numeric(4, 2),
    로튼토마토 int,
    왓챠 numeric(3, 2),
    primary key(제목, 감독),
    foreign key (제목) references DB2021_영화(제목) 
    on delete cascade 
    on update cascade
);

-- 배우 테이블
CREATE TABLE DB2021_배우(
    제목 varchar(40),
    감독 varchar(40),
    이름 varchar(40),
    생년월일 date,
    배역_이름 varchar(40),
    성별 varchar(10),
    직업 varchar(40),
    primary key(제목, 감독, 이름, 생년월일),
    foreign key (제목, 감독) references DB2021_영화(제목, 감독) 
    on delete cascade 
    on update cascade,
    check(성별 in ('여성','남성'))
);

CREATE INDEX idx_감독 ON DB2021_감독(이름);

-- 부원 테이블
CREATE TABLE DB2021_부원(
    학번 char(7),
    이름 varchar(40) not null,               
    비밀번호 varchar(10) not null,
    기수 int not null,                        
    직책 varchar(5) not null,                 
    primary key(학번),
    check(직책 in ('회장', '부회장', '부원', '비활동'))
);

-- 평가 테이블
CREATE TABLE DB2021_평가(
    학번 char(7) not null,
    이름 varchar(40),
    기수 int,
    제목 varchar(40) not null,
    감독 varchar(40) not null,
    평점 numeric(2, 1),
    평론 varchar(8000),
    비슷한_영화_제목 varchar(40),
    비슷한_영화의_감독 varchar(40),
    추천_감정 varchar(50),
    추천_날씨 varchar(50),
    추천_관계 varchar(50),
    primary key(학번, 제목, 감독),
    foreign key (학번) references DB2021_부원(학번) 
    on delete cascade 
    on update cascade,
    foreign key (제목, 감독) references DB2021_영화(제목, 감독) 
    on delete cascade 
    on update cascade,
    check(추천_감정 in ('심심할 때', '우울할 때', '잠이 안 올 때', '설레고 싶을 때')),
    check(추천_날씨 in ('맑은 날', '흐린 날', '눈 오는 날', '비 오는 날')),
    check(추천_관계 in ('혼자', '연인', '가족', '친구'))
);

CREATE INDEX idx_학번 ON DB2021_부원(이름);

